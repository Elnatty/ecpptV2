Hashes are an unordered, object-indexed collection of objects (or key-value pairs). Referred to as Dictionary in Python.

person = {"name"=>"Dking", "age":30}
person["age"] # fetches the age value.
person.index("Dking") # fetches the key ie, "name".
person.keys # returns all the keys.
person.value # returns all the values.
person.length
person.to_a # converts to an array.

